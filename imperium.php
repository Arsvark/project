<H2><u>L'Imperium de l'humanité</u></H2>
</div>  
<p class="text-md-center font-italic">« Nous luttons dans les ténèbres sans pouvoir vaincre. Le temps de la paix touche à sa fin et il est temps de mourir. Voici venir l'age de la guerre et nos derniers jours, nous les vivrons au milieu du fracas de notre propre destruction. »<br />
  </p>

  <p class="text-md-left">L’imperium est le nom du gouvernement humain, il règne sur la plupart des mondes humain, sa population est incalculable et son siège est la terre (appelé Terra).
    Son gouvernement est une théocratie féodale, théocratie car il à une religion d’état (le culte de l’empereur dieu) et féodal car les planètes disposent dans une certaine mesure d’une liberté de gouvernance totale. En bref imaginez le fief d’un seigneur au moyen-age tant que celui ci obéi à sont roi il est libre de gouverner comme sa lui chante.<br />
    <BR>
