    </div>
    <br>
    Pour les planètes, elles sont assez hétérogènes. Cela va de la planète ou ces habitants ont régresser à l’age de pierre aux mégalopoles d’immeubles haut de plusieurs kilomètres.<br />
    <BR>
    Paradoxalement l’Imperium est le gouvernement le moins humain qu’il soit.En effet les droits les plus élémentaire sont souvent ignoré, la liberté d’opinion réduite et l’espérance de vie au mieux aléatoire.
  </p>
  

