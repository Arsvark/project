<p class="text-md-left  bg">Au cours de ma longue carrière dans le milieu du Hobby, ma plus grande difficulté ne fut pas d'expliquer les règle. 
    En effet sauf de léger problème dû au matériel (dont la nécessité de posséder des sceau entier de dés), elle sont relativement simple a comprendre dans leur généralité.<br />
    <br>
    Non le vrai problème est d’expliquer de manière simple et compréhensible à des personnes qui y sont totalement étrangère comment dans un univers dans lequel se mélange un gouvernement humain autoritaire et obscurantiste, des soldats génétiquement modifié pouvant broyer de l’acier d’une main tout en crachant de l’acide et une dimension parallèle on naissent des démons qui sont le reflet de nos rêves et aspirations peut nous passionner (personnellement mon entré dans le hobby fut lorsque je vit la couverture de la troisième édition du livre de règle faite par le talentueux John Blanche).<br />
