<h2 class="display-4 text-center"><u>Introduction</u> </h2>
  <p class="text-md-left">Ah vous êtes toujours là, bon on va démarrer en douceur avec une petite introduction/mode d’emploi.
Donc vous désirez expliquer a votre famille ou à vos amis que non vous ne faites pas parti d’une secte et que non ces sachets d’herbes et cette colle ne sont pas de la drogue.<br />
Votre principal travail est de vous adapter a votre public pour cela voici quelques règle générale :<br />
    <ol>
      <li>Utiliser le moins possible les noms propres à l’univers qui ne sont pas transparent( le gouverneur planétaire c’est oui, échlésiarque beaucoup moins)</li>
      <li>Ne pas hésiter à utiliser des comparaison ou des métaphores simple(les armes à laser sont  comme dans les autres univers de SF ou le Warp est comme un océan avec ces zones calmes et ces tempêtes)</li>
      <li>Si on vous pose des questions éviter de trop détailler pour éviter de perdre votre auditoire( par exemple la sélection pour devenir  space marine  ce fait par un examen mortellement dangereux)</li>
    </ol>
    Dans les sections suivantes vous trouverez des définitions relativement généraliste sur certain éléments de l’univers.
